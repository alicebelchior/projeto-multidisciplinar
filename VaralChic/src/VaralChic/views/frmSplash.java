    public frmSplash() {
        initComponents();

        //configurar tudo do timer
        al = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {

                //se o progress bar < 100, continua lendo o valor e somando 2
                if(jProgressBar1.getValue() < 100) {
                    jProgressBar1.setValue(jProgressBar1.getValue() + 2);
                    
                } else {
                    //desligar o timer
                    t.stop();
                }
                
                if(jProgressBar1.getValue() == 98)
                {
                    //abrir a tela Principal
                    frmTelaLogin frml = new frmTelaLogin();
                    frml.setVisible(true);
                    dispose();
                }
                
                //modificando a lblCarregar
                if(jProgressBar1.getValue() == 20)
                    lblCarregar.setText("Carregando arquivos ...");
                if(jProgressBar1.getValue() == 40)
                    lblCarregar.setText("Carregando banco de dados ...");
                if(jProgressBar1.getValue() == 60)
                    lblCarregar.setText("Carregando cadastros ...");
                if(jProgressBar1.getValue() == 80)
                    lblCarregar.setText("Finalizando ...");
            }
        };
        
        //iniciar o timer
        t = new Timer(40, al); //40 = 40 ms; 1000 ms = 1 s
        t.start();
    }

